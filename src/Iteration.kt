fun main() {

    /* for */
    for (i in 1..10) {
        println(i)
    }

    /* while */
    var i:Int = 1;
    while (i <= 10){
        println(i)
        i++
    }

    /* do while */
    do {
        i--;
        println(i);
    }while (i>0)

}
